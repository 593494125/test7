package com.springboot.model.goods;

public class GoodsPic {

    private String spkh;
    private String sortid;
    private String picfile;
    private String defaultDisp;

    public String getSpkh() {
        return spkh;
    }

    public void setSpkh(String spkh) {
        this.spkh = spkh;
    }

    public String getSortid() {
        return sortid;
    }

    public void setSortid(String sortid) {
        this.sortid = sortid;
    }

    public String getPicfile() {
        return picfile;
    }

    public void setPicfile(String picfile) {
        this.picfile = picfile;
    }

    public String getDefaultDisp() {
        return defaultDisp;
    }

    public void setDefaultDisp(String defaultDisp) {
        this.defaultDisp = defaultDisp;
    }
}
