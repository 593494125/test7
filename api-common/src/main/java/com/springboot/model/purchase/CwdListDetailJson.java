package com.springboot.model.purchase;

import java.io.Serializable;

public class CwdListDetailJson implements Serializable,Cloneable{

    private String je;
    private String sm;
    private String sfkm;
    private String kmmc;//科目名称

    public String getJe() {
        return je;
    }

    public void setJe(String je) {
        this.je = je;
    }

    public String getSm() {
        return sm;
    }

    public void setSm(String sm) {
        this.sm = sm;
    }

    public String getSfkm() {
        return sfkm;
    }

    public void setSfkm(String sfkm) {
        this.sfkm = sfkm;
    }

    public String getKmmc() {
        return kmmc;
    }

    public void setKmmc(String kmmc) {
        this.kmmc = kmmc;
    }

    @Override
    public Object clone(){
        try {
            return (CwdListDetailJson)super.clone();
        } catch (CloneNotSupportedException e) {
            e.printStackTrace();
        }
        return null;
    }
}
